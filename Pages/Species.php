                <h1>
                    Косумельский енот
                </h1>
                <p>
                Косумельский енот — наименьший вид среди енотов. Взрослая особь имеет длину тела 60—80 см, при этом хвост — около 25 см, 
                самцы крупнее самок. Масса — от 3 до 4 кг. Тем самым косумельский енот примерно на 45 % легче и на 18 % меньше, чем материковый енот-полоскун.        
                Шерсть имеет коричнево-сероватый цвет, хвост — с жёлтым оттенком. На морде — чёрная маска.
                </p>
                <p>
                Вид является эндемиком Косумеля, небольшого острова в Карибском море у побережья Кинтана-Роо, площадь которого составляет 478 км². 
                Естественная среда обитания — мангровые леса, а также дождевые леса внутри острова, пляжи и возделываемые земли.
                Ведёт преимущественно одиночный ночной образ жизни. Всеяден. Около 50 % рациона енотов составляют крабы, а также лягушки, ящерицы и насекомые. 
                Из растительной пищи питается плодами манилкары и Pithecellobium и травой просо.
                </p>
