                <h1>
                    Гваделупский енот
                </h1>
                <p>
                Относится к вымирающему виду енотов, которые обитают только в заморских территориях Франции — Гваделупе в Карибском море (к юго-востоку от Пуэрто-Рико) 
                и на Малых Антильских островах. Несмотря на несогласие некоторых учёных, Procyon minor различают от принадлежащего к тому же виду Procyon lotor, 
                северного енота, обитающего в Северной Америке. Некоторые учёные не согласны с разделением вида по их географической среде обитания, 
                тем не менее другие учёные не согласны с некоторыми существенными различиями в биологии и анатомии этих существ. 
                Procyon minor является вымирающим видом и занесён в международный перечень сохранения природы и природных ресурсов.
                </p>
                <p>
                Гваделупский енот относится к сухопутным животным и обитает в основном во влажных областях Гваделупы. Енот предпочитает болотистые местности, 
                но может обитать и в сухом лесу — главным условием для проживания у него является наличие поблизости речки или озера. 
                Его жизненный период составляет примерно от 10 до 13 лет. В возрасте от 1 года енот считается взрослым. 
                Енот ведёт ночной образ жизни, а днём проводит время в пустых стволах, на ветвях деревьев и в тени листвы. 
                Взрослый енот может весить от 5,5 до 13,5 кг, питаясь фруктами, овощами, мясом, яйцами, 
                рыбой, которую сам ловит в воде, моллюсками, насекомыми и даже рептилиями. Брачный период у енотов приходится на январь-март. 
                После спаривания самка енота других самцов к себе не подпускает. Спустя 2 месяца она рожает на свет маленьких енотиков 
                (от двух до пяти особей). Место обитания нового семейства — дупла деревьев. 
                Внешне гваделупский енот почти ничем не отличается от енота-полоскуна. 
                Длина его тела — 50-60 см, хвост у него пушистый с 5-6 чёрными кольцами, всё тело покрыто густым мехом, а на голове «чёрная маска».
                </p>
