                        <h1>
                            Енот-полоскун
                        </h1>
                        <p>
                        Один из немногих видов, которые процветают в условиях усиления антропогенного воздействия, выражающегося в постепенном окультурировании угодий. 
                        Енот хорошо приживается в местах интродукции, хотя и является в целом теплолюбивым видом.
                        </p>
                        <p>
                        Енот-полоскун ростом с кошку. Длина тела 45—60 см, хвоста 20—25 см; масса 5—9 кг. 
                        Лапы короткие, с настолько развитыми пальцами, что следы похожи на отпечаток человеческой ладони. 
                        Енот может передними лапами захватывать и удерживать предметы, в том числе и мыть еду. Мех у енота густой, коричневато-серый.
                        В СССР акклиматизация енота проводилась в 1930-х годах в Приморье, затем вновь в 1950-х и 1980-x широкой полосой в европейской части в Литве, 
                        Белоруссии, на Донбассе, в Предкавказье, на Кавказе и в Азербайджане. Зверьки также выпускались в Средней Азии (Киргизия) и вновь в Приморье. 
                        В 1936 году в ореховых лесах Арсланбоба (Киргизия) был акклиматизован североамериканский енот-полоскун, где он охотно заселил дупла грецкого ореха. 
                        Широко распространился енот и в дельте Волги.
                        </p>
