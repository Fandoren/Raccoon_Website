                <h1>
                    Багамский енот
                </h1>
                <p>
                Внешне багамский енот мало отличим от подвидов енота-полоскуна с материка, однако меньше их в размерах, что, 
                видимо, является примером островной карликовости. Длина головы с телом 41,5—60 см, длина хвоста 20—40,5 см. 
                Самцы обычно крупнее самок. Окрас шкуры серый с лёгким оттенком охры на шее и передней части туловища. 
                Однако могут встречаться практически чёрные особи. На хвосте имеется от 5 до 10 цветных колец. Является эндемиком Багамских островов Карибского моря.
                </p>
                <p>
                Ведёт преимущественно ночной образ жизни. Активен круглый год. 
                Рацион состоит как из животной (мелкие беспозвоночные и позвоночные организмы), так и растительной (ягоды, орехи, семена) пищи. 
                Во время спаривания образует пары, в остальное время ведёт одиночный образ жизни.
                В 1996 году МСОП классифицировал подвид как находящийся под угрозой, так как количество взрослых особей было менее 2 500. 
                Из постоянного сокращения мест обитания багамского енота может постигнуть участь вымершего барбадосского енота. 
                Однако до сих пор сколь-нибудь эффективные меры для сохранения данного вида не предпринимаются.
                </p>
