                <h1>
                    Енот-ракоед
                </h1>
                <p>
                Агуара, или просто ракоед — вид енотов, происходящих из болот и джунглей Центральной и Южной Америки (включая Тринидад и Тобаго). 
                Хотя еноты этого вида и называются ракоедами, это не означает, что они питаются лишь ракообразными 
                — они стремятся искать и есть раков, лишь когда это возможно.
                </p>
                <p>
                Своим пушистым хвостом и рисунком на мехе вокруг глаз, образующим «бандитскую маску», он напоминает своего северного сородича, енота обыкновенного. 
                Енот-ракоед выглядит несколько меньше и имеет более обтекаемое тело, чем у енота обыкновенного, 
                так как шерсть первого короче, хотя тело покрыто ею примерно одинаково. Голова и тело составляют в длину от 41 до 60 см, длина хвоста от 20 до 42 см, 
                высота в холке около 23 см, а вес тела колеблется от 2 до 12 кг. 
                Самцы обычно крупнее самок. Шерсть енота-ракоеда на спине и на боках коричневая, а на брюшке серая, а на хвосте пять-шесть чёрно-белых колец. 
                Живут еноты-ракоеды в дикой природе более 5 лет.
                </p>
